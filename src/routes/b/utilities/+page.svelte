<p>根据提供的网页内容，该页面主要介绍了 Bootstrap 5
中的以下实用工具类（Utilities）：</p>
<h3 id="背景颜色类-background-colors">1. <strong>背景颜色类 (Background
Colors)</strong></h3>
<ul>
<li><code>.bg-primary</code>, <code>.bg-secondary</code>,
<code>.bg-success</code>, <code>.bg-danger</code>,
<code>.bg-warning</code>, <code>.bg-info</code>, <code>.bg-light</code>,
<code>.bg-dark</code>, <code>.bg-body</code>, <code>.bg-white</code>,
<code>.bg-transparent</code></li>
<li>渐变背景：<code>.bg-gradient</code></li>
</ul>
<h3 id="边框类-borders">2. <strong>边框类 (Borders)</strong></h3>
<ul>
<li>边框显示：<code>.border</code>, <code>.border-top</code>,
<code>.border-end</code>, <code>.border-bottom</code>,
<code>.border-start</code></li>
<li>边框隐藏：<code>.border-0</code>, <code>.border-top-0</code>,
<code>.border-end-0</code>, <code>.border-bottom-0</code>,
<code>.border-start-0</code></li>
<li>边框宽度：<code>.border-1</code> 到 <code>.border-5</code></li>
<li>边框颜色：<code>.border-primary</code>,
<code>.border-secondary</code>, <code>.border-success</code>,
<code>.border-danger</code>, <code>.border-warning</code>,
<code>.border-info</code>, <code>.border-light</code>,
<code>.border-dark</code>, <code>.border-white</code></li>
</ul>
<h3 id="圆角类-rounded-corners">3. <strong>圆角类 (Rounded
Corners)</strong></h3>
<ul>
<li>圆角形状：<code>.rounded</code>, <code>.rounded-top</code>,
<code>.rounded-end</code>, <code>.rounded-bottom</code>,
<code>.rounded-start</code>, <code>.rounded-circle</code>,
<code>.rounded-pill</code></li>
<li>圆角大小：<code>.rounded-0</code> 到 <code>.rounded-3</code></li>
</ul>
<h3 id="浮动类-float">4. <strong>浮动类 (Float)</strong></h3>
<ul>
<li>浮动方向：<code>.float-start</code>, <code>.float-end</code>,
<code>.float-none</code></li>
<li>响应式浮动：<code>.float-sm|md|lg|xl|xxl-start|end|none</code></li>
<li>清除浮动：<code>.clearfix</code></li>
</ul>
<h3 id="居中对齐类-center-alignment">5. <strong>居中对齐类 (Center
Alignment)</strong></h3>
<ul>
<li>水平居中：<code>.mx-auto</code></li>
</ul>
<h3 id="宽度和高度类-width-height">6. <strong>宽度和高度类 (Width &amp;
Height)</strong></h3>
<ul>
<li>宽度：<code>.w-25</code>, <code>.w-50</code>, <code>.w-75</code>,
<code>.w-100</code>, <code>.w-auto</code>, <code>.mw-100</code></li>
<li>高度：<code>.h-25</code>, <code>.h-50</code>, <code>.h-75</code>,
<code>.h-100</code>, <code>.h-auto</code>, <code>.mh-100</code></li>
</ul>
<h3 id="间距类-spacing">7. <strong>间距类 (Spacing)</strong></h3>
<ul>
<li>语法：<code>propertysides-size</code> 或
<code>propertysides-breakpoint-size</code></li>
<li>属性：<code>m</code>(margin), <code>p</code>(padding)</li>
<li>方向：<code>t</code>(top), <code>b</code>(bottom),
<code>s</code>(start/left), <code>e</code>(end/right),
<code>x</code>(left+right), <code>y</code>(top+bottom), 空白(全部)</li>
<li>大小：<code>0</code>, <code>1</code>, <code>2</code>,
<code>3</code>, <code>4</code>, <code>5</code>, <code>auto</code></li>
<li>断点：<code>sm</code>, <code>md</code>, <code>lg</code>,
<code>xl</code>, <code>xxl</code></li>
</ul>
<p>这些实用工具类可以帮助开发者快速实现各种样式效果，而无需编写额外的
CSS 代码。</p>
